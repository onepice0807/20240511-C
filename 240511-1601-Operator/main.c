#include <stdio.h>

int main() {
	unsigned char a = 2; // 00000010
	unsigned char b = 2; // 00000010
	unsigned char ret = 0; // 00000000

	// 컴퓨터는 뻴셈을 못합니다
	// 덧셈만 할 줄 압니다.

	// 보수연산
	ret = a - b;
	// ret = a + (-b);

	// 00000010
	// 11111101 : 1의 보수
	// 11111110 : 2의 보수

	// 00000010 : 2
	// 11111110 : -2
	// 00000000 : 0

	// 4 - 3 => 4 + (-3)
	// 00000011 : 3
	// 11111100 : 1의 보수
	// 11111101 : -3 (2의 보수)
	
   //  00000100  : 4
   //  11111101  : -3
   //  00000001  : 1

	return 0;
}